<?php namespace Titan\Event;

use Titan\Common\BagTrait;

class Event implements EventInterface
{
    use BagTrait;

    /**
     * @var string
     */
    private $name;

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }
}
